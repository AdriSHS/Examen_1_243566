    private void cargarDatosPaciente() {
        if (paciente != null) {
        lblNombrePaciente1.setText(paciente.getNombre());
        lblNombrePaciente2.setText(paciente.getNombre());
        lblNSSPaciente.setText(paciente.getNss()); // si tu Paciente tiene método getNss()
        lblEdad.setText(String.valueOf(paciente.getEdad()));
        lblDomicilio.setText(paciente.getDomicilio());
        lblSeguro.setText(paciente.isTieneSeguro() ? "Sí" : "No");
        lblCURP.setText(paciente.getCurp());
        lblTipoSangre.setText(paciente.getTipoSangre());
    }
   }
    
    private void cargarTablaMedicos() {
    DefaultTableModel modelo = (DefaultTableModel) TbMedicos.getModel();
    modelo.setRowCount(0); // Limpia filas existentes

    for (Medico medico : sistema.obtenerMedicos()) {
        Object[] fila = new Object[] {
            medico.getNombre(),
            medico.getEspecialidad(),
            medico.getConsultorio()
        };
        modelo.addRow(fila);
    }
}
