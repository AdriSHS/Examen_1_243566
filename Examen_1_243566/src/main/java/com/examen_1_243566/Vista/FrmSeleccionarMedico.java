    private void TbMedicosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TbMedicosMouseClicked
        int fila = TbMedicos.getSelectedRow();
        if (fila >= 0) {
        String nombre = TbMedicos.getValueAt(fila, 0).toString();

        // Buscar el objeto Medico por nombre
        Medico medicoSeleccionado = null;
        for (Medico m : sistema.obtenerMedicos()) {
            if (m.getNombre().equals(nombre)) {
                medicoSeleccionado = m;
                break;
            }
        }

        if (medicoSeleccionado != null) {
            
            lblTituloNombre.setText("Nombre del Medico:");
            lblTituloEdad.setText("Días Disponibles:");
            lblTituloSeguro.setText("Consultorio:");
            lblTituloCurp.setText("Horas Disponibles:");
            lblTituloInformacion.setText("Información del Medico");
            
            lblNombrePaciente2.setText(medicoSeleccionado.getNombre()); 
            lblEdad.setText(medicoSeleccionado.getDias()); 
            lblCURP.setText(medicoSeleccionado.getHoras()); 
            lblSeguro.setText(medicoSeleccionado.getConsultorio()); 
            
            lblConsultorio.setVisible(false);
            lblNombreMedico.setVisible(false);
            lblTituloNombreMedico.setVisible(false);
            lblTituloConsultorio.setVisible(false);
            lblDomicilio.setVisible(false);
            lblTituloDomicilio.setVisible(false);
            lblTipoSangre.setVisible(false);
            lblTituloTipoSangre.setVisible(false);
            lblTituloDiasYHoras.setVisible(false);
            lblDiasYHoras.setVisible(false);
            
            
            BtnAñadirCita.setVisible(true);
        }
      }
    }//GEN-LAST:event_TbMedicosMouseClicked

